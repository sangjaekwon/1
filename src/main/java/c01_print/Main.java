package c01_print;


public class Main {
    public static void main(String[] args) {
//        빠른 주석 command + /
        System.out.println("Hello, Java!");
        /**
         * 클래스 또는 매소드의 역할을 명시, 또는 설명하기 위한 주석
         */
        System.out.println(123);
        System.out.println("123");
        System.out.println(123+1);
        System.out.println("123" + "1");
    }
}

